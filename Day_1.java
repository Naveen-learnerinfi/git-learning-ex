package com.task;

public class Day_1 {
	public static void main(String[] args) {
		// Datatype variable name = value 
		
		// boolean - 1bits
		boolean present = true;
		System.out.println(present);
		
		//byte - 8bits
		byte age = 44;
		System.out.println(age);
		
		// character - 16 bits
		 char letter = 'A';
		 System.out.println(letter);
		 
		//short - 16 bits
		 short ph_cost = 21189;
		 System.out.println(ph_cost);
	
		// integer -32 bits 
		 int mb_no = 897894554;
		 System.out.println(mb_no);
		 
		 // long - 64 bits
		 long car_cost = 1454544519l;
		 System.out.println(car_cost);
		 
		 // float - 32 bits
		 float concentration  = 45.123467f;
		 System.out.println(concentration);
		 
		 // double - 64 bits
		 double magnitude = 3.7888945d;
		 System.out.println(magnitude);
		 
		 // String 
		 String name  = "Naveen";
		 System.out.println(name);
		 
		 
		 
	}
	

}
